export function renderPrice(prop) {
  if (!prop) return '별도 문의';
  if (!prop.price && !prop.deposit && !prop.monthlyRent) return '별도 문의';

  const tx = prop.transactionType?.[0];
  const fmt = (n) => (n ? `${n.toLocaleString()}만` : '0만');

  if (tx === '월세')
    return `보증금 ${fmt(prop.deposit)} / 월 ${fmt(prop.monthlyRent)} / 관리비 ${fmt(prop.maintenanceFee)}`;
  if (tx === '전세') return `전세 ${fmt(prop.deposit)}원`;
  if (tx === '매매') return `매매 ${fmt(prop.price)}원`;
  if (tx === '임대')
    return prop.deposit
      ? `보증금 ${fmt(prop.deposit)} / 월 ${fmt(prop.monthlyRent)}`
      : '별도 문의';
  return '별도 문의';
}

