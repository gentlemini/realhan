    상가: { bg: '#f0f4f8', color: '#3d5a80' },
    빌딩: { bg: '#2c2c2c', color: '#c19a6b' },
    토지: { bg: '#e8f5e9', color: '#2e7d32' },
    건물: { bg: '#f3e5f5', color: '#6a1b9a' },
